#!/usr/bin/env bash
set -euo pipefail

repo_root="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
tmp_dir="$(mktemp -d)"
trap 'rm -rf "$tmp_dir"' EXIT

mkdir -p "$tmp_dir/.dotfiles/home/.pi/agent/extensions/web-tools"
cp "$repo_root/dot" "$tmp_dir/.dotfiles/dot"
chmod +x "$tmp_dir/.dotfiles/dot"

cat > "$tmp_dir/.dotfiles/home/.zshrc" <<'EOF'
export DOTFILES_TEST=1
EOF
cat > "$tmp_dir/.dotfiles/home/.pi/README.md" <<'EOF'
# pi
EOF

mkdir -p "$tmp_dir/.dotfiles/home/.pi/node_modules/.bin"
printf 'package binary\n' > "$tmp_dir/.dotfiles/home/.pi/node_modules/.bin/pi"

mkdir -p "$tmp_dir/.dotfiles/home/.pi/agent/extensions/web-tools/dist"
printf 'generated dist\n' > "$tmp_dir/.dotfiles/home/.pi/agent/extensions/web-tools/dist/index.js"
mkdir -p "$tmp_dir/.dotfiles/home/.pi/agent/extensions/web-tools/out"
printf 'generated out\n' > "$tmp_dir/.dotfiles/home/.pi/agent/extensions/web-tools/out/index.js"
mkdir -p "$tmp_dir/.dotfiles/home/.pi/agent/extensions/web-tools/coverage"
printf 'generated coverage\n' > "$tmp_dir/.dotfiles/home/.pi/agent/extensions/web-tools/coverage/lcov.info"
mkdir -p "$tmp_dir/.dotfiles/home/.pi/agent/extensions/web-tools/logs"
printf 'generated logs\n' > "$tmp_dir/.dotfiles/home/.pi/agent/extensions/web-tools/logs/run.log"
mkdir -p "$tmp_dir/.dotfiles/home/.pi/agent/extensions/web-tools/.cache"
printf 'generated cache\n' > "$tmp_dir/.dotfiles/home/.pi/agent/extensions/web-tools/.cache/state.json"
mkdir -p "$tmp_dir/.dotfiles/home/.pi/agent/extensions/web-tools/build"
printf 'generated build\n' > "$tmp_dir/.dotfiles/home/.pi/agent/extensions/web-tools/build/tsconfig.cjs.tsbuildinfo"

mkdir -p "$tmp_dir/.pi/node_modules/.bin"
printf 'existing binary\n' > "$tmp_dir/.pi/node_modules/.bin/pi"
mkdir -p "$tmp_dir/.pi/agent/extensions/web-tools/dist"
printf 'existing dist\n' > "$tmp_dir/.pi/agent/extensions/web-tools/dist/index.js"
mkdir -p "$tmp_dir/.pi/agent/extensions/web-tools/out"
printf 'existing out\n' > "$tmp_dir/.pi/agent/extensions/web-tools/out/index.js"
mkdir -p "$tmp_dir/.pi/agent/extensions/web-tools/coverage"
printf 'existing coverage\n' > "$tmp_dir/.pi/agent/extensions/web-tools/coverage/lcov.info"
mkdir -p "$tmp_dir/.pi/agent/extensions/web-tools/logs"
printf 'existing logs\n' > "$tmp_dir/.pi/agent/extensions/web-tools/logs/run.log"
mkdir -p "$tmp_dir/.pi/agent/extensions/web-tools/.cache"
printf 'existing cache\n' > "$tmp_dir/.pi/agent/extensions/web-tools/.cache/state.json"
mkdir -p "$tmp_dir/.pi/agent/extensions/web-tools/build"
printf 'existing build\n' > "$tmp_dir/.pi/agent/extensions/web-tools/build/tsconfig.cjs.tsbuildinfo"

HOME="$tmp_dir" "$tmp_dir/.dotfiles/dot" stow >"$tmp_dir/stow.log" 2>&1

[[ -L "$tmp_dir/.zshrc" ]]
[[ -L "$tmp_dir/.pi/README.md" ]]
[[ -f "$tmp_dir/.pi/node_modules/.bin/pi" ]]
[[ -f "$tmp_dir/.pi/agent/extensions/web-tools/dist/index.js" ]]
[[ -f "$tmp_dir/.pi/agent/extensions/web-tools/out/index.js" ]]
[[ -f "$tmp_dir/.pi/agent/extensions/web-tools/coverage/lcov.info" ]]
[[ -f "$tmp_dir/.pi/agent/extensions/web-tools/logs/run.log" ]]
[[ -f "$tmp_dir/.pi/agent/extensions/web-tools/.cache/state.json" ]]
[[ -f "$tmp_dir/.pi/agent/extensions/web-tools/build/tsconfig.cjs.tsbuildinfo" ]]

[[ ! -L "$tmp_dir/.pi/node_modules/.bin/pi" ]]
[[ ! -L "$tmp_dir/.pi/agent/extensions/web-tools/dist/index.js" ]]
[[ ! -L "$tmp_dir/.pi/agent/extensions/web-tools/out/index.js" ]]
[[ ! -L "$tmp_dir/.pi/agent/extensions/web-tools/coverage/lcov.info" ]]
[[ ! -L "$tmp_dir/.pi/agent/extensions/web-tools/logs/run.log" ]]
[[ ! -L "$tmp_dir/.pi/agent/extensions/web-tools/.cache/state.json" ]]
[[ ! -L "$tmp_dir/.pi/agent/extensions/web-tools/build/tsconfig.cjs.tsbuildinfo" ]]

echo "stow generated pi artifacts regression passed"
